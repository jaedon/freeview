int is_alphanum(u_char uc)
{
	return(rmtype[uc] & 0xC0);
}


int is_num(u_char uc)
{
	return(rmtype[uc] & 0x80);
}


int is_alpha(u_char	uc)
{
	return(rmtype[uc] & 0x40);
}

int is_paren(u_char	uc)
{
	return(rmtype[uc] & 0x20);
}
