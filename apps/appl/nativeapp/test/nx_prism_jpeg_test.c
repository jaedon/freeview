		i = SCALE_FACTOR_NUM - 1;
	cinfo.scale_denom = 8;
	cinfo.scale_num   = i + 1;
	jpeg->fn_jpeg_start_decompress(&cinfo);

	row_width = cinfo.output_width * cinfo.num_components;

	*output_width = cinfo.output_width;
	*output_height = cinfo.output_height;
