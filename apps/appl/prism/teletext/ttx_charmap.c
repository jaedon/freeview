HERROR	TTX_CHAR_ToUCS2 (const TTX_Cell_t *pCell, HUINT16 *pusUCS2, HUINT16 *pusDiaMark)
{
	HERROR	err;

	if (pCell == NULL || pusUCS2 == NULL || pusDiaMark == NULL || eTTX_CODE_LAST <= pCell->ucCode)
	{
		return ERR_FAIL;
	}

	*pusDiaMark = 0;

	if (pCell->bCompositeChar)
	{
		err	= local_ttxchar_CompositeCharTo(pusUCS2, pCell);
		if (err != ERR_OK)
		{
			err =	local_ttxchar_DiacriticalMark(pusDiaMark, pCell);
			err |=	local_ttxchar_CharTo(pusUCS2, pCell);
		}
	}
	else
	{
		err	= local_ttxchar_CharTo(pusUCS2, pCell);
	}

	return (err == ERR_OK) ? ERR_OK : ERR_FAIL;
}


HERROR	TTX_CHAR_ToUnicode (HUINT16 *pusUnicode, const HUINT8 ucCharCode,
							const HUINT8 ucNationalOption, const HUINT8 ucChar)
{
	const HUINT16	*aCharMap = NULL;

	if (pusUnicode == NULL
		|| eTTX_CODE_LAST <= ucCharCode
		|| ucChar < 0x20 || 0x7F < ucChar)
	{
		return ERR_FAIL;
	}

	aCharMap	= sc_TTXCodeToUnicode[ucCharCode];
	if (aCharMap == NULL)
	{
		return ERR_FAIL;
	}

	if (ucCharCode == eTTX_CODE_G0_LATIN)
	{
		HINT32	lIndex	= -1;

		switch (ucChar)
		{
		case 0x23:	lIndex = 0;		break;
		case 0x24:	lIndex = 1;		break;
		case 0x40:	lIndex = 2;		break;
		case 0x5B:	lIndex = 3;		break;
		case 0x5C:	lIndex = 4;		break;
		case 0x5D:	lIndex = 5;		break;
		case 0x5E:	lIndex = 6;		break;
		case 0x5F:	lIndex = 7;		break;
		case 0x60:	lIndex = 8;		break;
		case 0x7B:	lIndex = 9;		break;
		case 0x7C:	lIndex = 10;	break;
		case 0x7D:	lIndex = 11;	break;
		case 0x7E:	lIndex = 12;	break;
		}

		if (lIndex >= 0 && ucNationalOption < eTTX_NOS_EMPTY)
		{
			*pusUnicode	= sc_LatinNationalOptionSubset[ucNationalOption][lIndex];
			return ERR_OK;
		}
	}

	*pusUnicode	= aCharMap[ucChar - 0x20];

	return ERR_OK;
}


HERROR	TTX_CHAR_CompositeCharToUnicode	(
						HUINT16 *		pusUnicode,
						const HUINT8 	ucCharCode,
						const HUINT8 	ucChar,
						const HUINT8 	ucDiacriticalMark
						)
{

	switch (ucCharCode)
	{
	case eTTX_CODE_G0_LATIN:
		if (isupper(ucChar))
		{
			*pusUnicode = sc_CompositeChar_Latin_UPPER[ucChar - 'A'][ucDiacriticalMark];
		}
		else if (islower(ucChar))
		{
			*pusUnicode = sc_CompositeChar_Latin_LOWER[ucChar - 'a'][ucDiacriticalMark];
		}
		else if (ucDiacriticalMark == 0)
		{
			*pusUnicode = sc_G0_Latin[ucChar - 0x20];
		}
		else
		{
			*pusUnicode = 0x0000;
		}
		break;

	default:
		*pusUnicode = 0x0000;
	}

	if (*pusUnicode == 0x0000)
	{
		return ERR_FAIL;
	}

	return ERR_OK;
}


