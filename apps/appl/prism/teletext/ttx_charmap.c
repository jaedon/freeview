static HERROR	local_ttxchar_DiacriticalMark (HUINT16 *pusDiaMark, const TTX_Cell_t *pCell)
{
	HUINT8	ucChar	= pCell->Opt.ucDiacriticalMark + 0x40;

	if (ucChar < 0x20 || 0x7F < ucChar)
	{
		return ERR_FAIL;
	}

	*pusDiaMark	= sc_G2_Latin[(HINT32)(ucChar - 0x20)];

	return ERR_OK;
}


static HERROR	local_ttxchar_G0LatinNOSTo (HUINT16 *pusUnicode, const TTX_Cell_t *pCell)
{
	HINT32	nNosIndex;
	HUINT8	ucChar	= (pCell->ucChar < 0x20 || 0x7F < pCell->ucChar) ? 0x20 : pCell->ucChar;

	switch (ucChar)
	{
	case 0x23:	nNosIndex = 0;	break;
	case 0x24:	nNosIndex = 1;	break;
	case 0x40:	nNosIndex = 2;	break;
	case 0x5B:	nNosIndex = 3;	break;
	case 0x5C:	nNosIndex = 4;	break;
	case 0x5D:	nNosIndex = 5;	break;
	case 0x5E:	nNosIndex = 6;	break;
	case 0x5F:	nNosIndex = 7;	break;
	case 0x60:	nNosIndex = 8;	break;
	case 0x7B:	nNosIndex = 9;	break;
	case 0x7C:	nNosIndex = 10;	break;
	case 0x7D:	nNosIndex = 11;	break;
	case 0x7E:	nNosIndex = 12;	break;
	default:
		return ERR_FAIL;
	}

	if (eTTX_NOS_EMPTY <= pCell->ucNationalOption)
		return ERR_FAIL;

	*pusUnicode = sc_LatinNationalOptionSubset[(HINT32)pCell->ucNationalOption][nNosIndex];

	return (*pusUnicode == 0) ? ERR_FAIL : ERR_OK;
}



static HERROR	local_ttxchar_G1MosaicTo (HUINT16 *pusUnicode, const TTX_Cell_t *pCell)
{
	HUINT16	usMosaic;
	HUINT8	ucChar	= (pCell->ucChar < 0x20 || 0x7F < pCell->ucChar) ? 0x20 : pCell->ucChar;

	if (!(ucChar & 0x20))
		return ERR_FAIL;

	if (pCell->ucChar == 0x20)
	{
		usMosaic	= 0x0020;
	}
	else
	{
		usMosaic	= (pCell->Opt.bSeparatedMosaic) ? 0xF740 : 0xF700;
		if (0x20 < ucChar && ucChar <= 0x3F)
			usMosaic	+= (HUINT16)(ucChar - 0x20);
		else	// 0x60 ~ 0x7F
			usMosaic	+= (HUINT16)(ucChar - 0x40);
	}

	*pusUnicode	= usMosaic;

	return ERR_OK;
}


static HERROR	local_ttxchar_UserDefinedTo (HUINT16 *pusUnicode, const TTX_Cell_t *pCell)
{
	switch (pCell->ucChar)
	{
	case eTTX_CHAR_USER_ROTATE:
		*pusUnicode	= 0x21BA;
		break;

	default:
		return ERR_FAIL;
	}
	return ERR_OK;
}


static HERROR	local_ttxchar_CharTo (HUINT16 *pusUnicode, const TTX_Cell_t *pCell)
{
	HUINT8			ucChar	= (pCell->ucChar < 0x20 || 0x7F < pCell->ucChar) ? 0x20 : pCell->ucChar;
	const HUINT16	*ausMap;

	ausMap	= sc_TTXCodeToUnicode[(HINT32)pCell->ucCode];
	if (ausMap == NULL)
	{
		if (pCell->ucCode == eTTX_CODE_G1_MOSAIC)
			return local_ttxchar_G1MosaicTo(pusUnicode, pCell);
		if (pCell->ucCode == eTTX_CODE_USER_DEFINED)
			return local_ttxchar_UserDefinedTo(pusUnicode, pCell);
		return ERR_FAIL;
	}
	else
	{
		if (pCell->ucCode == eTTX_CODE_G0_LATIN && local_ttxchar_G0LatinNOSTo(pusUnicode, pCell) == ERR_OK)
			return ERR_OK;

		*pusUnicode	= ausMap[(HINT32)(ucChar - 0x20)];
	}

	return (*pusUnicode == 0) ? ERR_FAIL : ERR_OK;
}



static HERROR	local_ttxchar_CompositeCharTo (HUINT16 *pusUnicode, const TTX_Cell_t *pCell)
{
	HUINT8	ucChar;
	HUINT8	ucDiaMark;

	ucChar		= (pCell->ucChar < 0x20 || 0x7F < pCell->ucChar) ? 0x20 : pCell->ucChar;
	ucDiaMark	= pCell->Opt.ucDiacriticalMark;

	switch (pCell->ucCode)
	{
	case eTTX_CODE_G0_LATIN:
		if ('A' <= ucChar && ucChar <= 'Z' /*isupper(ucChar)*/)
		{
			*pusUnicode = sc_CompositeChar_Latin_UPPER[ucChar - 'A'][ucDiaMark];
		}
		else if ('a' <= ucChar && ucChar <= 'z' /*islower(ucChar)*/)
		{
			*pusUnicode = sc_CompositeChar_Latin_LOWER[ucChar - 'a'][ucDiaMark];
		}
		else if (ucDiaMark == 0)
		{
			*pusUnicode = sc_G0_Latin[ucChar - 0x20];
		}
		else
		{
			*pusUnicode = 0x0000;
		}
		break;

	default:
		*pusUnicode = 0x0000;
	}

	return (*pusUnicode == 0x0000) ? ERR_FAIL : ERR_OK;
}

