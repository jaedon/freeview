static void 	local_ttxdrcs_CopyPTU(HUINT8* pucDestPTU, const HUINT8* pucSrc)
{
	HINT32	i, j;

	if (pucDestPTU == NULL || pucSrc == NULL)
	{
		return;
	}

	for (i = 0 ; i < TTX_PTU_LENGTH ; i++)
	{
		for (j = 0; j < 6; j++)
		{
			pucDestPTU[i * 6 + j] = (pucSrc[i] & (0x20 >> j)) ;
		}
	}
}


static void		local_ttxdrcs_CompositePTU (HUINT8 *pucDestPTU, const HUINT8 *pucSrc)
{
	HINT32	i, j;

	if (pucDestPTU == NULL || pucSrc == NULL)
	{
		return;
	}

	for (i = 0 ; i < TTX_PTU_LENGTH ; i++)
	{
		for (j = 0; j < 6; j++)
		{
			pucDestPTU[i * 6 + j] |= (pucSrc[i] & (0x20 >> j)) ;
		}
	}
}

static const HUINT8 *local_ttxdrcs_GetDefaultPtuByIndex (HUINT16 usFontIndex)
{
	if (usFontIndex >= TTX_BMP_DRCS_COUNT)
	{
		return NULL;
	}
	return &s_defaultDRCS[usFontIndex * TTX_PTU_LENGTH];
}


static HERROR	local_ttxdrcs_GetG1PTU (const TTX_Cell_t *pCell, HUINT8 *pucPTU)
{
	const HUINT16	*pG1;

	if (!(pCell->ucChar & 0x20) || pCell->ucChar < 0x20 || 0x7F < pCell->ucChar)
	{
		return ERR_FAIL;
	}

	pG1 = (pCell->Opt.bSeparatedMosaic) ? s_mosaicG1_S : s_mosaicG1_C;

	HxSTD_MemSet(pucPTU, 0, TTX_PTU_LENGTH * 6);

	if (pCell->ucChar & 0x01)
	{
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(pG1[0]));
	}
	if (pCell->ucChar & 0x02)
	{
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(pG1[1]));
	}
	if (pCell->ucChar & 0x04)
	{
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(pG1[2]));
	}
	if (pCell->ucChar & 0x08)
	{
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(pG1[3]));
	}
	if (pCell->ucChar & 0x10)
	{
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(pG1[4]));
	}
	if (pCell->ucChar & 0x40)
	{
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(pG1[5]));
	}
	return ERR_OK;
}


static HERROR	local_ttxdrcs_GetUserDefinedPTU (const TTX_Cell_t *pCell, HUINT8 *pucPTU)
{
	const HUINT8 *pucSrc;

	if (pCell->ucChar >= eTTX_USERDEF_BMP_LAST)
	{
		return ERR_FAIL;
	}

	pucSrc	= local_ttxdrcs_GetDefaultPtuByIndex(s_userDefine[pCell->ucChar]);
	if (pucSrc == NULL)
	{
		return ERR_FAIL;
	}

	local_ttxdrcs_CopyPTU(pucPTU, pucSrc);

	return ERR_OK;
}


static HINT32	local_ttxdrcs_GetNosIndex (const TTX_Cell_t *pCell)
{
	HINT32	nNosIndex;

	switch (pCell->ucChar)
	{
	case 0x23:	nNosIndex = 0;	break;
	case 0x24:	nNosIndex = 1;	break;
	case 0x40:	nNosIndex = 2;	break;
	case 0x5B:	nNosIndex = 3;	break;
	case 0x5C:	nNosIndex = 4;	break;
	case 0x5D:	nNosIndex = 5;	break;
	case 0x5E:	nNosIndex = 6;	break;
	case 0x5F:	nNosIndex = 7;	break;
	case 0x60:	nNosIndex = 8;	break;
	case 0x7B:	nNosIndex = 9;	break;
	case 0x7C:	nNosIndex = 10;	break;
	case 0x7D:	nNosIndex = 11;	break;
	case 0x7E:	nNosIndex = 12;	break;
	default:
		return -1;
	}

	if (eTTX_NOS_EMPTY < pCell->ucNationalOption)
		return -1;

	return nNosIndex;
}

static HERROR	local_ttxdrcs_GetDefaultPTU (const TTX_Cell_t *pCell, HUINT8 *pucPTU)
{
	const HUINT16	*pMap;
	const HUINT8	*pucSrc;
	HUINT16			usIndex	= 0xFFFF;

	if (pCell->ucCode >= eTTX_CODE_LAST)
	{
		return ERR_FAIL;
	}

	pMap	= s_bmpIndirectIndex[pCell->ucCode];
	if (pMap == NULL)
	{
		switch (pCell->ucCode)
		{
		case eTTX_CODE_G1_MOSAIC:
			return local_ttxdrcs_GetG1PTU(pCell, pucPTU);
		case eTTX_CODE_USER_DEFINED:
			return local_ttxdrcs_GetUserDefinedPTU(pCell, pucPTU);
		default:
			break;
		}
		return ERR_FAIL;
	}

	if (pCell->ucChar < 0x20 || 0x7F < pCell->ucChar)
	{
		return ERR_FAIL;
	}

	if (pCell->ucCode == eTTX_CODE_G0_LATIN)
	{
		HINT32 nNosIndex	= local_ttxdrcs_GetNosIndex(pCell);
		if (nNosIndex >= 0)
		{
			usIndex = s_latinNOS[pCell->ucNationalOption][nNosIndex];
		}
	}

	if (usIndex == 0xFFFF)
	{
		usIndex	= pMap[(HINT32)(pCell->ucChar - 0x20)];
	}

	pucSrc	= local_ttxdrcs_GetDefaultPtuByIndex(usIndex);
	if (pucSrc == NULL)
	{
		return ERR_FAIL;
	}

	local_ttxdrcs_CopyPTU(pucPTU, pucSrc);

	if (pCell->bCompositeChar)
	{
		usIndex = s_latinG2[pCell->Opt.ucDiacriticalMark + 0x20];
		local_ttxdrcs_CompositePTU(pucPTU, local_ttxdrcs_GetDefaultPtuByIndex(usIndex));
	}
	return ERR_OK;
}


