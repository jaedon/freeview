
#ifndef _HxANSICODE_H_
#define _HxANSICODE_H_

#include <stdio.h>
#include <stdlib.h>
#include <assert.h>
#include <stdarg.h>


#define HxANSI_CODE_BEGIN			"\033["
#define HxANSI_CODE_END				"m"
#define	HxANSI_CODE_NORMAL			"0"
#define	HxANSI_CODE_BOLD			"1"
#define	HxANSI_CODE_UNDERLINED		"4"
#define	HxANSI_CODE_BLINKING		"5"
#define	HxANSI_CODE_REVERSE			"7"

#define HxANSI_COLOR_FG_BLACK		"30"
#define HxANSI_COLOR_FG_RED			"31"
#define HxANSI_COLOR_FG_GREEN		"32"
#define HxANSI_COLOR_FG_YELLOW 		"33"
#define HxANSI_COLOR_FG_BLUE		"34"
#define HxANSI_COLOR_FG_PURPLE 		"35"
#define HxANSI_COLOR_FG_CYAN		"36"
#define HxANSI_COLOR_FG_WHITE		"37"

#define HxANSI_COLOR_BG_BLACK		"40"
#define HxANSI_COLOR_BG_RED			"41"
#define HxANSI_COLOR_BG_GREEN		"42"
#define HxANSI_COLOR_BG_YELLOW 		"43"
#define HxANSI_COLOR_BG_BLUE		"44"
#define HxANSI_COLOR_BG_PURPLE 		"45"
#define HxANSI_COLOR_BG_CYAN		"46"
#define HxANSI_COLOR_BG_WHITE		"47"

#define HxANSI_CODE_RESET				HxANSI_CODE_BEGIN HxANSI_CODE_NORMAL HxANSI_CODE_END
#define	HxANSI_CODE(s,code,fg)			HxANSI_CODE_BEGIN code";"fg HxANSI_CODE_END s HxANSI_CODE_RESET
#define	HxANSI_CODE_SIMPLE(s,fg)		HxANSI_CODE_BEGIN HxANSI_CODE_BOLD";"fg HxANSI_CODE_END s HxANSI_CODE_RESET
#define	HxANSI_CODE_FULL(s,code,fg,bg)	HxANSI_CODE_BEGIN code";"fg";"bg HxANSI_CODE_END s HxANSI_CODE_RESET

#define	HxANSI_COLOR_BLACK(msg)		HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_BLACK)
#define	HxANSI_COLOR_RED(msg)		HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_RED)
#define	HxANSI_COLOR_GREEN(msg)		HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_GREEN)
#define	HxANSI_COLOR_YELLOW(msg)	HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_YELLOW)
#define	HxANSI_COLOR_BLUE(msg)		HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_BLUE)
#define	HxANSI_COLOR_PURPLE(msg)	HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_PURPLE)
#define	HxANSI_COLOR_CYAN(msg)		HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_CYAN)
#define	HxANSI_COLOR_WHITE(msg)		HxANSI_CODE_SIMPLE(msg, HxANSI_COLOR_FG_WHITE)

#endif /* _HxANSICODE_H_ */

