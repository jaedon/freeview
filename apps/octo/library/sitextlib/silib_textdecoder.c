static HBOOL	sx_text_Semaphore (HBOOL lock)
{
	static HUINT32 s_semaphore;
	if (s_semaphore == 0)
	{
		(void) HxSEMT_Create(&s_semaphore, "SIxTEXT", HxSEMT_FIFO);
	}
	if (lock)
		return HxSEMT_Get(s_semaphore) == 0 ? TRUE : FALSE;
	return HxSEMT_Release(s_semaphore) == 0 ? TRUE : FALSE;
}

static HBOOL	sx_text_Lock (void)
{
	return sx_text_Semaphore(TRUE);
}

static void		sx_text_Unlock (void)
{

	(void)sx_text_Semaphore(FALSE);
}

