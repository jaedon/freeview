static HUINT32	sx_text_Validate300468 (HUINT8 *dst, HUINT32 dstLen, const HCHAR *langCode, const HUINT8 *src, HUINT32 srcLen)
{
	HUINT8	prefix[4];
	HUINT32	prefixLen;
	HUINT32	needLen;

	return_if(dst == NULL || dstLen == 0, 0);
	return_if(src == NULL || srcLen == 0, 0);

	prefixLen = 0;

	if (*src >= 0x20) // !start_with(character coding table prefix)
	{
		if (langCode)
		{
			(void) sx_text_GetCodingTablePrefix(prefix, &prefixLen, HLIB_LANG_639ToId(langCode));
		}
		if (prefixLen == 0)
		{
			prefix[0] = 0x10; prefix[1] = 0x69; prefix[2] = 0x37;
			prefixLen = 3;
		}
	}

	if (*src == 0x11) // UCS2
	{
		needLen = srcLen + (srcLen & 0x1);
		return_if(dstLen < needLen + 1, 0);

		memcpy(dst, src, needLen + 1);
		dst[needLen - 1] = '\0';
		dst[needLen]     = '\0';
	}
	else if (prefixLen == 0)
	{
		needLen = srcLen;
		return_if(dstLen < needLen + 1, 0);
		memcpy(dst, src, needLen);
		dst[needLen] = '\0';
	}
	else
	{
		needLen = srcLen + prefixLen;
		return_if(dstLen < needLen + 1, 0);
		memcpy (dst, prefix, prefixLen);
		memcpy (dst + prefixLen, src, srcLen);
		dst[needLen] = '\0';
	}
	return sx_text_UkDvbStrPostProcessing(dst, needLen);
}

