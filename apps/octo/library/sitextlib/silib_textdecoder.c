static HBOOL sx_text_GetBit (const char *str, unsigned int pos)
{
	// This code could be improved, but works
	// Get the byte containing the relevant bit
	unsigned int byte_pos = pos/8;
	char byte = str[byte_pos];

	// Work out the bit we want
	unsigned int bit_shift = 7 - (pos - (byte_pos*8));

	// Do an AND to return true if it is set, else false
	if  (byte & (1 << bit_shift))
		return TRUE;
	else
		return FALSE;
}

static unsigned char sx_text_GetNext8Bits(const unsigned char *str,unsigned int pos)
{
	int i;
	// This code could be improved, but works

	// Use INT_FSAT_GetBit multiple times and build up the byte bit by bit
	unsigned char result = sx_text_GetBit((const char *)str,pos);
	for (i=1; i<8; ++i)
	{
		result = result << 1;
		if (sx_text_GetBit((const char *)str,pos+i))
			result+=1;
	}
	return result;
}

