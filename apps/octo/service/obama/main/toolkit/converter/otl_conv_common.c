SVC_SatType_t OTL_CONV_SatString2SVC_SatType (const HCHAR *name)
{
	HINT32	i, n;

	n = sizeof(s_aSatTypeMap) / sizeof(s_aSatTypeMap[0]);
	for (i = 0 ; i < n ; i++)
	{
		if (HLIB_STD_StrCaseCmp(name, s_aSatTypeMap[i].szString) == 0)
			return s_aSatTypeMap[i].nEnum;
	}
	return eSatType_NONE;
}

const HCHAR *	OTL_CONV_SVC_SatType2String (SVC_SatType_t type)
{
	HINT32	i, n;

	n = sizeof(s_aSatTypeMap) / sizeof(s_aSatTypeMap[0]);
	for (i = 0 ; i < n ; i++)
	{
		if (s_aSatTypeMap[i].nEnum == type)
			return s_aSatTypeMap[i].szString;
	}

	//해당하는 값이 없으면 "NONE" return
	return s_aSatTypeMap[i-1].szString;
}


