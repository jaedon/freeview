BUS_Callback_t	pmgr_ColdBoot_GetProc(void)
{
#if defined(CONFIG_OP_MIDDLE_EAST)
	return xproc_coldboot_MeSat;
#elif defined(CONFIG_OP_SES)
	return xproc_coldboot_Ses;
#else
	return xproc_coldboot_Base;
#endif
}

HERROR	pmgr_ColdBoot_Start(void)
{
#if defined(CONFIG_OP_MIDDLE_EAST)
	return xmgr_coldboot_Start_MeSat();
#elif defined(CONFIG_OP_SES)
	return xmgr_coldboot_Start_Ses();
#else
	return xmgr_coldboot_Start_Base();
#endif
}

