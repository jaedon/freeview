HERROR	xmgr_tplist_GetCabDefaultTpListModel_Base (MgrTplist_Model_e *peModelType)
{
	HxCountry_e			eCountryId = eCountryID_NONE;
	DxOperator_e		eOperatorType;
	HERROR				hErr;

	if (NULL == peModelType)
		return ERR_FAIL;

	MGR_SPEC_GetCountryCode (&eCountryId);
	HxLOG_Warning(HxANSI_COLOR_CYAN("eCountryId    = 0x%x\n"), eCountryId);
	hErr = DB_PARAM_GetItem(eDB_PARAM_ITEM_OPERATOR_TYPE, (HUINT32 *)&eOperatorType, 0);
	if (ERR_OK != hErr)
	{
		HxLOG_Warning("DB_PARAM_GetItem(eDB_PARAM_ITEM_OPERATOR_TYPE) Error!!\n");
	}
	HxLOG_Warning(HxANSI_COLOR_CYAN("eOperatorType = %d\n"), eOperatorType);

	switch (eCountryId)
	{
	case eCountryID_DEU:
		switch(eOperatorType)
		{
		case eDxOPERATOR_KDG: 	*peModelType = eTPLIST_MODEL_KDG_BASE;		break;
		case eDxOPERATOR_SkyD:	*peModelType = eTPLIST_MODEL_SKYD_BASE;		break;
		default:				*peModelType = eTPLIST_MODEL_GERMAN_BASE;	break;
		}
		break;

	case eCountryID_SWE:
		switch(eOperatorType)
		{
		case eDxOPERATOR_Telenor: 	*peModelType = eTPLIST_MODEL_TELENOR;	break;
		default:					*peModelType = eTPLIST_MODEL_TELENOR;	break;
		}
		break;

	case eCountryID_DNK:
		switch(eOperatorType)
		{
		case eDxOPERATOR_Tdc:	*peModelType = eTPLIST_MODEL_TDC;	break;
		default:				*peModelType = eTPLIST_MODEL_TDC;	break;
		}
		break;
	default:
		*peModelType = eTPLIST_MODEL_DEFAULT;
		break;
	}

	return ERR_OK;
}

HERROR	xmgr_tplist_GetCabDefaultTpList_Base(MgrTplist_Model_e eModelType, MgrTplist_Type_e eTplistType, MgrTplist_CabTsInfo_t **ppstTsList, HUINT32 *pulTsCount)
{
	HUINT32		 		 ulCount;
	HUINT32				 ulArraySize;
	CAB_TsInfoList_t	*pstTsInfoList = NULL;
	MgrTplist_CabTsInfo_t		*pstTsList = NULL;

	if (NULL == pulTsCount)
		return ERR_FAIL;

	ulArraySize = 0;
	switch (eTplistType)
	{
	case eTPLIST_TYPE_AUTO:
		pstTsInfoList = s_astCableAutoTp;
		ulArraySize = sizeof(s_astCableAutoTp)/sizeof(CAB_TsInfoList_t);
		break;

	case eTPLIST_TYPE_LINEAR:
		pstTsInfoList = s_astCableLinearTp;
		ulArraySize = sizeof(s_astCableLinearTp)/sizeof(CAB_TsInfoList_t);
		break;

	default:
	return ERR_FAIL;
	}


	pstTsList = NULL;
	for (ulCount = 0; ulCount < ulArraySize; ulCount++)
	{
		if (pstTsInfoList[ulCount].eFreqModel == eModelType)
		{
			HxLOG_Info("freq model found!! (%d)\n", eModelType);
			pstTsList = pstTsInfoList[ulCount].pstTpArray;
			if (NULL != pstTsList)
			{
				break;
			}
		}
	}

	if (NULL == pstTsList)
	{
		if (pulTsCount)		*pulTsCount = 0;
		return ERR_OK;
	}

	if (pulTsCount)
	{
		ulCount = 0;
		while (1)
		{
			if (pstTsList[ulCount].freq == 0)
			{
				break;
			}

			ulCount++;
		}
		*pulTsCount = ulCount;
	}

	if (ppstTsList)
	{
		*ppstTsList = pstTsList;
	}

	HxLOG_Info("eModelType = %d, eTplistType = %d, ulCount = %d\n", eModelType, eTplistType, ulCount);

	return ERR_OK;
}

HERROR	xmgr_tplist_GetCabDefaultTpList_Count (MgrTplist_Model_e eModelType, MgrTplist_Type_e eTplistType, HUINT32 *pulTsCount)
{
	return xmgr_tplist_GetCabDefaultTpList_Base(eModelType, eTplistType, NULL, pulTsCount);
}

