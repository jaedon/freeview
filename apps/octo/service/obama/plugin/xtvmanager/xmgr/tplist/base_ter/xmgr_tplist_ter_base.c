HERROR xmgr_tplist_GetVHFBandIII_Base(HxCountry_e eCountryId, HUINT32 ulFrequency, MgrTplist_TerTsInfo_t *pstTsInfo)
{
	MgrTplist_TerTsInfo_t stVhfBandIIIForItaly[] =
	{
		ITA_CH06, ITA_CH07, ITA_CH08, ITA_CH09,	// VHF Band III 추가
	};

	if(pstTsInfo == NULL)
	{
		return ERR_FAIL;
	}

	if(eCountryId == eCountryID_ITA)
	{
		if(ulFrequency == stVhfBandIIIForItaly[0].ulFreq)
		{
			HxSTD_memcpy(pstTsInfo, &stVhfBandIIIForItaly[0], sizeof(MgrTplist_TerTsInfo_t));
			return ERR_OK;
		}
		else if(ulFrequency == stVhfBandIIIForItaly[1].ulFreq)
		{
			HxSTD_memcpy(pstTsInfo, &stVhfBandIIIForItaly[1], sizeof(MgrTplist_TerTsInfo_t));
			return ERR_OK;
		}
		else if(ulFrequency == stVhfBandIIIForItaly[2].ulFreq)
		{
			HxSTD_memcpy(pstTsInfo, &stVhfBandIIIForItaly[2], sizeof(MgrTplist_TerTsInfo_t));
			return ERR_OK;
		}
		else if(ulFrequency == stVhfBandIIIForItaly[3].ulFreq)
		{
			HxSTD_memcpy(pstTsInfo, &stVhfBandIIIForItaly[3], sizeof(MgrTplist_TerTsInfo_t));
			return ERR_OK;
		}
	}

	return ERR_FAIL;
}

