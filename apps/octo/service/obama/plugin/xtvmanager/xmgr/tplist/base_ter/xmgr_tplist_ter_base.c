#else
			*pastTsList = (MgrTplist_TerTsInfo_t*) &s_astDefaultFreqForEuropeUHF;
			*pulNumTs = (HUINT32)(sizeof(s_astDefaultFreqForEuropeUHF)/sizeof(MgrTplist_TerTsInfo_t));
#endif
			break;

		case eCountryID_NOR: //NORDIG
		case eCountryID_SWE:
		case eCountryID_DNK:
		case eCountryID_FIN:
			*pastTsList = (MgrTplist_TerTsInfo_t*) &s_astDefaultFreqForNordig;
			*pulNumTs = (HUINT32)(sizeof(s_astDefaultFreqForNordig)/sizeof(MgrTplist_TerTsInfo_t));
			break;

		case eCountryID_FRA:
			*pastTsList = (MgrTplist_TerTsInfo_t*) &s_astDefaultFreqForFrance ;
			*pulNumTs = (HUINT32)(sizeof(s_astDefaultFreqForFrance)/sizeof(MgrTplist_TerTsInfo_t));
			break;

		case eCountryID_USA :
			*pastTsList = (MgrTplist_TerTsInfo_t*) &s_astDefaultFreqForUs;
			*pulNumTs = (HUINT32)(sizeof(s_astDefaultFreqForUs)/sizeof(MgrTplist_TerTsInfo_t));
			break;

		case eCountryID_VNM: //OIRT
		case eCountryID_CZE :
		case eCountryID_SVK :
		case eCountryID_EST :
		case eCountryID_HUN :
		case eCountryID_LTU :
		case eCountryID_POL :
		case eCountryID_RUS :  // Russia, Fredaration 통일.
