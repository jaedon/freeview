HERROR CA_NA_BdcRegisterPopUpFunc(TBdcDisplayPopUp	pfnDisplayPopUp,	TBdcRemovePopUp		  pfnRemovePopUp)
{
	if ( (pfnDisplayPopUp == NULL) || (pfnRemovePopUp == NULL) )
	{
		HxLOG_Info("Targets are null...\n");
		return ERR_FAIL;
	}

	HxLOG_Print("(%p, %p)\n", pfnDisplayPopUp, pfnRemovePopUp);

	g_pfnDisplayPopUpCallback = pfnDisplayPopUp;
	g_pfnRemovePopUpCallback = pfnRemovePopUp;
	return ERR_OK;
}


HERROR CA_NA_BdcUnregisterPopUpFunc(void)
{
	HxLOG_Print("\n");

	g_pfnDisplayPopUpCallback = NULL;
	g_pfnRemovePopUpCallback = NULL;
	return ERR_OK;
}


