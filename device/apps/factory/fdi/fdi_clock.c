FDI_ERR_CODE FDI_CLOCK_GetCurrentTime(unsigned long *pulTime)
{
	DI_ERR_CODE eDiErr;
	
	eDiErr = DI_RTC_GetCurrentTime(pulTime);
	if( eDiErr != DI_ERR_OK )
	{
		return FDI_ERR;
	}
	return FDI_NO_ERR;
}

