static UT_MENU_ITEM_T	menu_ci[] = 
{
	MENU_START,
		
	MENU_END,
};

static UT_MENU_ITEM_T	menu_main_ci[] = 
{	
	MENU_SUB(menu_ci),
};

void UT_CI_Init(void)
{
	HINT32 i;
	HINT32	array_size;

	array_size = sizeof(menu_main_ci)/sizeof(menu_main_ci[0]);
	for (i=0; i<array_size; i++)
	{
		UT_MENU_AddMainMenu(&menu_main_ci[i]);
	}
	array_size = sizeof(menu_ci)/sizeof(menu_ci[0]);
	for (i=0; i<array_size; i++)
	{
		UT_MENU_AddMenu(&menu_ci[i]);
	}
}



