use strict;

my $file = shift @ARGV;
open(OUTFILE, ">$file") or die "Unable to open output file $file";

my $moduleUpper;
print OUTFILE "#####################################################\n";
print OUTFILE "# This file is automatically generated.  Do not edit.\n";
print OUTFILE "#####################################################\n";

print OUTFILE "\$(NEXUS_SYNCTHUNK_DIR)/exists:\n";
print OUTFILE "\t\$(Q_)\$(MKDIR) \$@\n";
print OUTFILE "\n";
print OUTFILE "CLASS_LIST=\$(NEXUS_SYNCTHUNK_DIR)/class_list.inc\n";
print OUTFILE "CLASS_VERIFICATION=\$(NEXUS_SYNCTHUNK_DIR)/nexus_class_verification.h\n";
print OUTFILE "PUBLIC_INCLUDES := ";
foreach $moduleUpper (@ARGV) {
    next if ($moduleUpper eq 'BASE');
    print OUTFILE "\$(addsuffix /*.h,\$(NEXUS_$moduleUpper\_PUBLIC_INCLUDES)) ";
}
print OUTFILE "\n";

# Generate nexus_class_verification.h using PERL
print OUTFILE "NEXUS_SERVER_MODULES=\$(filter-out \$(NEXUS_CLIENT_MODULES),\$(NEXUS_MODULES))\n";
print OUTFILE "\$(CLASS_LIST) \$(CLASS_VERIFICATION): \n";
print OUTFILE "\t\$(Q_)cd \$(NEXUS_TOP)/build/tools/kernelproxy && \$(PERL) class_list.pl \$(CLASS_LIST) \$(CLASS_VERIFICATION) -headers \$(PUBLIC_INCLUDES) -modules \$(NEXUS_SERVER_MODULES)\n";
print OUTFILE "\n";

foreach $moduleUpper (@ARGV) {
    my $moduleLower = lc $moduleUpper;
    my $thunked = 1;
    my $proxy = 1;

    print OUTFILE "#\n# $moduleUpper\n#\n";
    print OUTFILE "\$(NEXUS_OBJ_DIR)/$moduleUpper/exists:\n";
    print OUTFILE "\t\$(Q_)\$(MKDIR) \$@\n";
    print OUTFILE "\n";

    # Skip thunks for BASE -- it is an exception
    if ( ($moduleUpper eq 'BASE') ) {
       $thunked = 0;
       $proxy = 0;
    }

    if ($thunked == 1) {
        # Thunk Info
        print OUTFILE "NEXUS_$moduleUpper\_SYNCTHUNKS := \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_thunks.c \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_thunks.h\n";
        print OUTFILE "\n";
        print OUTFILE "\$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_thunks.c: \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_thunks.h\n";
        print OUTFILE "\n";
        # if there's no proxy, we still need nexus_class_verification.h for the stubs
        print OUTFILE "\$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_thunks.h: \$(wildcard \$(addsuffix /*.h,\$(NEXUS_$moduleUpper\_PUBLIC_INCLUDES))) \$(NEXUS_SYNCTHUNK_DIR)/exists \$(CLASS_VERIFICATION)\n";
        print OUTFILE "\t\@echo \"[Thunk..... $moduleLower]\"\n";
        print OUTFILE "\t\$(Q_)cd \$(NEXUS_TOP)/build/tools/syncthunk && \$(PERL) bapi_build.pl $moduleUpper \$(NEXUS_SYNCTHUNK_DIR) \$(addsuffix /*.h,\$(NEXUS_$moduleUpper\_PUBLIC_INCLUDES))\n";
        print OUTFILE "\n";
    }
    else {
        print OUTFILE "# $moduleUpper does not have syncthunks\n\n";
    }

    if ($proxy == 1) {
        print OUTFILE "NEXUS_$moduleUpper\_PROXY := \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_proxy.c \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_ioctl.h\n";
        print OUTFILE "ifeq (\${NEXUS_MODE},driver)\n";
        print OUTFILE "NEXUS_$moduleUpper\_DRIVER := \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_driver.c \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_kernel_export.c \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_ioctl.h\n";
        print OUTFILE "NEXUS_$moduleUpper\_OBJECTS += \$(NEXUS_OBJ_DIR)/$moduleUpper/nexus_$moduleLower\_driver.\$(NEXUS_OBJ_SUFFIX) \$(NEXUS_OBJ_DIR)/$moduleUpper/nexus_$moduleLower\_kernel_export.\$(NEXUS_OBJ_SUFFIX)\n";
        print OUTFILE "endif\n";
        print OUTFILE "\n";
        print OUTFILE "\$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_kernel_export.c: \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_ioctl.h\n";
        print OUTFILE "\$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_driver.c: \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_kernel_export.c \$(CLASS_VERIFICATION)\n";
        print OUTFILE "\n";
        print OUTFILE "\$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_ioctl.h: \$(NEXUS_SYNCTHUNK_DIR)/exists \$(wildcard \$(addsuffix /*.h,\$(NEXUS_$moduleUpper\_PUBLIC_INCLUDES))) \$(CLASS_LIST)\n";
        print OUTFILE "\t\@echo \"[Proxy..... $moduleLower]\"\n";
        print OUTFILE "\t\$(Q_)cd \$(NEXUS_TOP)/build/tools/kernelproxy && \$(PERL) bapi_build.pl $moduleUpper \$(NEXUS_SYNCTHUNK_DIR) \$(CLASS_LIST) \$(addsuffix /*.preload,\$(NEXUS_$moduleUpper\_PUBLIC_INCLUDES)) \$(addsuffix /*.h,\$(NEXUS_$moduleUpper\_PUBLIC_INCLUDES))\n";
        print OUTFILE "\n";
    }
    else {
        print OUTFILE "# $moduleUpper does not have proxy layer\n\n";
    }

    # Module Rules
    print OUTFILE "ifdef NEXUS_$moduleUpper\_PRECOMPILED_HEADER\n";
    print OUTFILE "\$(NEXUS_OBJ_DIR)/$moduleUpper/nexus_$moduleLower\_precompiled.h.gch: \$(NEXUS_$moduleUpper\_PRECOMPILED_HEADER) \${NEXUS_OBJ_DIR}/$moduleUpper/exists \$(NEXUS_$moduleUpper\_SYNCTHUNKS)\n";
    print OUTFILE "\t\@echo \"[Compile... \$(notdir \$<) ($moduleLower)]\"\n";
    print OUTFILE "\t\$(Q_)\$(CC) \$(CDEP_FLAG) \$(NEXUS_CFLAGS) \$(NEXUS_CFLAGS_BPROFILE) \$(NEXUS_$moduleUpper\_CFLAGS) -c \$< -o \$@\n";
    print OUTFILE "\n";
    print OUTFILE "\$(NEXUS_$moduleUpper\_OBJECTS): \$(NEXUS_OBJ_DIR)/$moduleUpper/%.\$(NEXUS_OBJ_SUFFIX): %.c \${NEXUS_OBJ_DIR}/$moduleUpper/exists \${NEXUS_OBJ_DIR}/$moduleUpper/nexus_$moduleLower\_precompiled.h.gch \$(NEXUS_$moduleUpper\_SYNCTHUNKS)\n";
    print OUTFILE "\t\@echo \"[Compile... \$(notdir \$<) ($moduleLower)]\"\n";
    print OUTFILE "\t\$(Q_)\$(CC) \$(if \$(filter \$<, \$(NEXUS_$moduleUpper\_SYNCTHUNKS) \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_proxy.c \$(NEXUS_SYNCTHUNK_DIR)/nexus_$moduleLower\_kernel_export.c),, -include \${NEXUS_OBJ_DIR}/$moduleUpper/nexus_$moduleLower\_precompiled.h) \$(CDEP_FLAG) \$(NEXUS_CFLAGS) \$(NEXUS_CFLAGS_BPROFILE) \$(NEXUS_$moduleUpper\_CFLAGS) -c \$< -o \$@\n";
    print OUTFILE "\n";
    print OUTFILE "else\n";
    print OUTFILE "\$(NEXUS_$moduleUpper\_OBJECTS): \$(NEXUS_OBJ_DIR)/$moduleUpper/%.\$(NEXUS_OBJ_SUFFIX): %.c \${NEXUS_OBJ_DIR}/$moduleUpper/exists \$(NEXUS_$moduleUpper\_SYNCTHUNKS) \${NEXUS_PRECOMPILED_HEADER_H}.gch\n";
    print OUTFILE "\t\@echo \"[Compile... \$(notdir \$<) ($moduleLower)]\"\n";
    print OUTFILE "\t\$(Q_)\$(CC) \$(CDEP_FLAG) -include \${NEXUS_PRECOMPILED_HEADER_H} \$(NEXUS_CFLAGS) \$(NEXUS_CFLAGS_BPROFILE) \$(NEXUS_$moduleUpper\_CFLAGS) -c \$< -o \$@\n";
    print OUTFILE "\n";
    print OUTFILE "endif\n";

}
close OUTFILE;

