use strict;

package bapi_kernel_export;

sub generate
{
	my ($filename, $module, @funcs) = @_;
	my $func;
	open FILE, ">$filename";

	print FILE "/*********************************\n";
	print FILE "*\n";
	print FILE "* This file is autogeneratedby the Nexus Platform makefile.\n";
	print FILE "*\n";
	print FILE "*********************************/\n";
	print FILE "#define NEXUS_THUNK_LAYER\n";
	print FILE "#include \"nexus_${module}_module.h\"\n";
	print FILE "#include <linux/version.h>\n";
	print FILE "#if LINUX_VERSION_CODE >= KERNEL_VERSION(2, 6, 37)\n";
	print FILE "#include <generated/autoconf.h>\n";
	print FILE "#else\n";
	print FILE "#include <linux/autoconf.h>\n";
	print FILE "#endif\n";
	print FILE "#include <linux/module.h>\n";
	print FILE "\n";	

	for $func (@funcs) {
		print FILE "EXPORT_SYMBOL($func->{FUNCNAME});\n";
	}
	print FILE "\n\n\n";
	close FILE;
}

1;
