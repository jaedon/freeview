/***************************************************************************
Summary:
Try to figure out UTC offset from the TSID using the TSID table. 
***************************************************************************/
int tsid_to_utc_offset(unsigned short tsid, int *p_offset, char *p_dst)
{
	unsigned int i;
	const utc_offset_t *p_tsid_table = g_utc_offset;
	for (i = 0; i < sizeof(g_utc_offset); ++i)
	{
		if (tsid == p_tsid_table[i].tsid)
		{
			*p_offset = p_tsid_table[i].offset * 3600;
			*p_dst = p_tsid_table[i].dst;
			return 0;
		}
	}
	return -1;
}
